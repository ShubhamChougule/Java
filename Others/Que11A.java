class Que11A {
    public static void main(String[] shubham) {
        // implicit casting
        
        // int to long
        int A = 1023;
        long B = A;
        System.out.println("int : " + A);
        System.out.println("long : " + B);

        // float to double
        float X = 12.33f;
        double Y = X;
        System.out.println("float : " + X);
        System.out.println("double : " + Y);
    }
}