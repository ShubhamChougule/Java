class Que11B {
    public static void main(String[] shubham) {
        // Explicit casting
        
        // int to byte
        int A = 123;
        byte B = (byte) A;
        System.out.println("int : " + A);
        System.out.println("Byte : " + B);

        // float to double
        float X = 384.34f;
        int Y = (int)X;
        System.out.println("float : " + X);
        System.out.println("int : " + Y);
    }
}